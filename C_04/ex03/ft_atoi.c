int	ft_atoi(char *str)
{
	int	i;
	int	res;
	int	neg;

	neg = 1;
	res = 0;
	i = 0;
	while ((str[i] >= 9 & str[i] <= 13) || str[i] == 32)
		i++;
	while (str[i] == 43 || str[i] == 45)
	{
		if (str[i] == 45)
			neg = -neg;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		res = res * 10 + (str[i] - 48);
		i++;
	}
	res *= neg;
	return (res);
}
